
public class TacGia {
	private String tenTacGia;
	private Date date;
	public TacGia(String tenTacGia, Date date) {
		
		this.tenTacGia = tenTacGia;
		this.date = date;
	}
	public String getTenTacGia() {
		return tenTacGia;
	}
	public void setTenTacGia(String tenTacGia) {
		this.tenTacGia = tenTacGia;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	
	
	
}
